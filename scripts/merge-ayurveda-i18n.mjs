/**
 * Deep-merge ayurvedaPage / ayurvedaQuiz / ayurvedaDash into locale JSON files.
 * Run: node scripts/merge-ayurveda-i18n.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const localesDir = path.join(__dirname, '../src/i18n/locales');

function deepMerge(base, override) {
  const out = { ...base };
  for (const key of Object.keys(override)) {
    const b = base[key];
    const o = override[key];
    if (
      o !== null &&
      typeof o === 'object' &&
      !Array.isArray(o) &&
      b !== null &&
      typeof b === 'object' &&
      !Array.isArray(b)
    ) {
      out[key] = deepMerge(b, o);
    } else {
      out[key] = o;
    }
  }
  return out;
}

function mergeFile(name, overlay) {
  const p = path.join(localesDir, name);
  const j = JSON.parse(fs.readFileSync(p, 'utf8'));
  const merged = deepMerge(j, overlay);
  fs.writeFileSync(p, JSON.stringify(merged, null, 2) + '\n');
}

