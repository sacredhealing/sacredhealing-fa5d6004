-- ============================================
-- RLS Policies for mql_strategies
-- ============================================
-- Users can view public strategies and their own strategies
CREATE POLICY "Users can view public strategies"
ON public.mql_strategies FOR SELECT
USING (is_public = true OR created_by = auth.uid());

-- Users can create their own strategies
CREATE POLICY "Users can create strategies"
ON public.mql_strategies FOR INSERT
WITH CHECK (created_by = auth.uid());

-- Users can update their own strategies
CREATE POLICY "Users can update own strategies"
ON public.mql_strategies FOR UPDATE
USING (created_by = auth.uid())
WITH CHECK (created_by = auth.uid());

-- Admins can do everything
CREATE POLICY "Admins can manage all strategies"
ON public.mql_strategies FOR ALL
USING (public.has_role(auth.uid(), 'admin'))
WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- ============================================
-- RLS Policies for mql_signals
-- ============================================
-- Users can view signals from strategies they own or are subscribed to
CREATE POLICY "Users can view relevant signals"
ON public.mql_signals FOR SELECT
USING (
  EXISTS (
    SELECT 1 FROM public.mql_strategies s
    WHERE s.id = mql_signals.strategy_id
    AND (s.created_by = auth.uid() OR s.is_public = true)
  )
  OR EXISTS (
    SELECT 1 FROM public.user_strategy_subscriptions uss
    WHERE uss.strategy_id = mql_signals.strategy_id
    AND uss.user_id = auth.uid()
    AND uss.status = 'active'
  )
);

-- System can insert signals (via edge functions)
CREATE POLICY "System can insert signals"
ON public.mql_signals FOR INSERT
WITH CHECK (true); -- Edge functions will handle auth

