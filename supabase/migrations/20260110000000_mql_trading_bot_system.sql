-- Function to update updated_at timestamp
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Triggers for updated_at
CREATE TRIGGER update_mql_strategies_updated_at
  BEFORE UPDATE ON public.mql_strategies
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_mql_trades_updated_at
  BEFORE UPDATE ON public.mql_trades
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_user_bot_connections_updated_at
  BEFORE UPDATE ON public.user_bot_connections
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

CREATE TRIGGER update_user_strategy_subscriptions_updated_at
  BEFORE UPDATE ON public.user_strategy_subscriptions
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

-- Function to calculate trade duration when closed
CREATE OR REPLACE FUNCTION public.calculate_trade_duration()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.status = 'closed' AND OLD.status != 'closed' AND NEW.closed_at IS NOT NULL THEN
    NEW.duration_seconds = EXTRACT(EPOCH FROM (NEW.closed_at - NEW.opened_at))::INTEGER;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER calculate_mql_trade_duration
  BEFORE UPDATE ON public.mql_trades
  FOR EACH ROW
  EXECUTE FUNCTION public.calculate_trade_duration();

